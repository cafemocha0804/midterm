function plus(a, b){
    let result;
    result= a + b;
    return result;
}

function minus(a, b){
    let result;
    result= a - b;
    return result;
}

function times(a, b){
    let result;
    result= a * b;
    return result;
}

function div(a, b){
    let result;
    result= a / b;
    return result;
}

function mod(a, b){  
    let result;
    result= a % b;
    return result;
}
